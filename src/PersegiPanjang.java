public class PersegiPanjang {
    public static void main(String[] args) {
        System.out.println("Hitunglah luas persegi panjang");
        System.out.println("dengan panjang = 20cm , dan lebar = 25cm ");
        Double Panjang = 20.0;
        Double Lebar = 25.0;
        Double Luas = Panjang*Lebar;

        System.out.println("Jadi luas persegi panjangnya adalah "+Luas+"cm");
    }
}