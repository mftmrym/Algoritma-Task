public class KelilingLIngkaran {
    public static void main(String[] args) {
        System.out.println("Hitunglah Keliling lingkaran dengan Jari-Jari = 28cm");
        Double Jari_Jari = 28.0;
        Double Keliling = 2 * (22.0/7) * Jari_Jari;

        System.out.println("Jadi keliling lingkarannya adalah "+Keliling+"cm");
    }
}
