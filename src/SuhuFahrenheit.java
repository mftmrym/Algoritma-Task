public class SuhuFahrenheit {
    public static void main(String[] args) {
        System.out.println("Hitunglah suhu Fahrenheit dari suhu Celcius 20 C");
        Double Celcius = 20.0;
        Double Fahrenheit = Celcius * 9/5 + 32;
        System.out.println("Jadi suhu dalam fahrenheitnya adalah = "+Fahrenheit+"F");
    }
}
